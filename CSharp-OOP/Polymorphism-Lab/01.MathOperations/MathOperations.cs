namespace Operations
{
   public class MathOperations
    {
        public int Add(int x,int y)
        {
            return x + y;
        }
        public double Add(double x,double y,double n)
        {
            return x + y + n;
        }
        public decimal Add(decimal x,decimal y,decimal n)
        {
            return x + y + n;
        }
    }
}
